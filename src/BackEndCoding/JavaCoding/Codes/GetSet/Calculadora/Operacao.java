package BackEndCoding.JavaCoding.Codes.GetSet.Calculadora;

public class Operacao {

    public static int somar(int num1, int num2){int resultado = num1 + num2; return resultado;}
    public static int subtrair (int num1, int num2){ return  num1 - num2; }
    public static int multiplicar (int num1, int num2){ return num1 * num2; }
    public static int dividir(int num1, int num2){ return num1 / num2; }
    public static int resto (int num1, int num2){ return num1 % num2; }

}
