package BackEndCoding.JavaCoding.Codes.GetSet.Caluladora;

public class Calculadora {
    Double a, b;
    String Resposta() {
        return "\nA resposta é: ";
    }
    Double Adicao() {
        return a + b;
    }
    Double Multiplicacao() {
        return a * b;
    }
    Double Divisao() {
        return a / b;
    }

    Double Resto() {
        return a % b;
    }
    Double Subtracao() {
        return a - b;
    }
}