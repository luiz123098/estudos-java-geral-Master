package BackEndCoding.JavaCoding.EspressoesMatematicas;

public class ProgramaDeSoma {
    public static void main(String[] args) {
//SOMATORIO DE EQUAÇÕES
        int a   = -5 + 8 * 6;
        int b   = (55+9) % 9;
        int c   =   20 + -3*5 / 8;
        int d   =   5 + 15 / 3 * 2 - 8 % 3;
        System.out.println("A sum: "+a);
        System.out.println("B sum: "+b);
        System.out.println("C sum: "+c);
        System.out.println("D sum: "+d);
    }
}
